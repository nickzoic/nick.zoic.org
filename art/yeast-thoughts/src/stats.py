from math import sqrt

samples = [
    (10, 1000000),
    (20, 2000000),
    (50, 5000000),
    (100, 10000000),
    (1, 1000000),
    (2, 2000000),
    (5, 5000000),
    (10, 10000000),
    (0, 1000000),
]

for x, n in samples:
    p = x / n
    s = sqrt(p*(1-p)/n)
    print("%10d %10d %.9f %.9f %.9f %.9f" % ( x, n, p, s, p-s, p+s ))
